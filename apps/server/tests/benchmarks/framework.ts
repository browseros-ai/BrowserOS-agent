/**
 * Benchmark framework for BrowserOS agent speed/token/efficiency comparisons.
 *
 * Token estimation strategy (no server-side token log needed):
 *   - Tool outputs → LLM input context: chars / 4  (standard ~4 chars/token)
 *   - Tool call args → LLM output:      chars / 4
 *   - Each screenshot image:            +1 000 tokens (vision pricing estimate)
 *
 * These are lower-bound estimates — they do not include the system prompt or
 * message history prefix that accumulates across steps. The delta between
 * v1 and v2 is the meaningful signal, not the absolute number.
 */

import { Agent } from '@browseros-ai/agent-sdk'

/** Approximate token cost per screenshot image (vision pricing, 1280×720 PNG). */
const SCREENSHOT_TOKEN_ESTIMATE = 1_000

/** Full metrics for one benchmark run. */
export interface BenchmarkMetrics {
  scenario: string
  version: 'v1_baseline' | 'v2_optimized'
  wallClockMs: number
  llmStepCount: number
  toolCallCount: number
  toolCalls: string[]
  snapshotCount: number
  screenshotCount: number
  /** Estimated tokens: tool outputs fed back as LLM input (chars/4 + images×1000). */
  estInputTokens: number
  /** Estimated tokens: tool call args generated by LLM (chars/4). */
  estOutputTokens: number
  estTotalTokens: number
  estAvgTokensPerStep: number
  /** Raw char counts behind the estimates. */
  outputChars: number
  inputChars: number
  success: boolean
  error?: string
}

// ---------------------------------------------------------------------------
// SSE event types (UIMessageStreamEvent subset we care about)
// ---------------------------------------------------------------------------

interface ToolInputStartEvent {
  type: 'tool-input-start'
  toolName?: string
  toolCallId?: string
}

interface ToolInputAvailableEvent {
  type: 'tool-input-available'
  toolCallId?: string
  toolName?: string
  input?: unknown
}

interface ToolOutputAvailableEvent {
  type: 'tool-output-available'
  toolCallId?: string
  output?: unknown
}

type AnyEvent = {
  type: string
} & Partial<
  ToolInputStartEvent & ToolInputAvailableEvent & ToolOutputAvailableEvent
>

