// Helpers
// ---------------------------------------------------------------------------

/**
 * Extract text length and image count from a tool output.
 * BrowserOS tool outputs have shape: { content: Array<{type,text}|{type,data,mimeType}> }
 */
function measureOutput(output: unknown): { chars: number; images: number } {
  let chars = 0
  let images = 0

  if (!output || typeof output !== 'object') {
    chars = String(output ?? '').length
    return { chars, images }
  }

  const o = output as Record<string, unknown>

  // Standard ToolResult content array
  if (Array.isArray(o.content)) {
    for (const item of o.content as Array<Record<string, unknown>>) {
      if (item.type === 'text' && typeof item.text === 'string') {
        chars += item.text.length
      } else if (item.type === 'image') {
        images++
        // base64 data length as a chars proxy (image tokens use fixed estimate instead)
      }
    }
    return { chars, images }
  }

  // Fallback: stringify everything
  chars = JSON.stringify(o).length
  return { chars, images }
}

