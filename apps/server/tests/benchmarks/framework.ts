export interface RunOptions {
  serverUrl: string
  scenario: string
  version: 'v1_baseline' | 'v2_optimized'
  task: string
  setup?: (agent: Agent) => Promise<void>
  /** Maximum agent steps allowed (default: 30). */
  maxSteps?: number
  /** Timeout for the whole run in ms (default: 120 000). */
  timeoutMs?: number
}

// ---------------------------------------------------------------------------
// runBenchmark
// ---------------------------------------------------------------------------

export async function runBenchmark(
  opts: RunOptions,
): Promise<BenchmarkMetrics> {
  const {
    serverUrl,
    scenario,
    version,
    task,
    setup,
    maxSteps = 30,
    timeoutMs = 120_000,
  } = opts

  // Use ANTHROPIC_API_KEY or OPENAI_API_KEY from env to bypass the
  // BrowserOS free-tier daily quota when benchmarking on a remote server.
  const llmConfig = process.env.ANTHROPIC_API_KEY
    ? {
        provider: 'anthropic' as const,
        apiKey: process.env.ANTHROPIC_API_KEY,
        model: 'claude-haiku-4-5-20251001',
      }
    : process.env.OPENAI_API_KEY
      ? {
          provider: 'openai' as const,
          apiKey: process.env.OPENAI_API_KEY,
          model: 'gpt-4o-mini',
        }
      : undefined

  const agent = new Agent({ url: serverUrl, llm: llmConfig })

  // Run setup (nav calls) before registering onProgress so synthetic nav
  // start-step/finish-step events don't pollute the LLM step counter.
  if (setup) {
    try {
      await setup(agent)
    } catch {
      // non-fatal — act() may still work on whatever page is loaded
    }
  }

  let llmStepCount = 0
  let inputChars = 0 // tool output text → becomes LLM input
  let outputChars = 0 // tool call args   → generated by LLM
  let imageCount = 0 // screenshots in tool outputs
  const toolCalls: string[] = []

  // Register AFTER setup so nav() events are excluded from metrics
  agent.onProgress((raw: unknown) => {
    const ev = raw as AnyEvent

    if (ev.type === 'start-step') {
      llmStepCount++
    } else if (ev.type === 'tool-input-start' && ev.toolName) {
      toolCalls.push(ev.toolName)
    } else if (ev.type === 'tool-input-available' && ev.input) {
      outputChars += JSON.stringify(ev.input).length
    } else if (ev.type === 'tool-output-available' && ev.output !== undefined) {
      const { chars, images } = measureOutput(ev.output)
      inputChars += chars
      imageCount += images
    }
  })

  const startMs = Date.now()
  let success = false
  let error: string | undefined

  try {
    const timeout = setTimeout(() => {}, timeoutMs)
    try {
      const effectiveTask =
        version === 'v1_baseline' ? `${BASELINE_TASK_PREFIX}${task}` : task
      await agent.act(effectiveTask, { maxSteps })
      success = true
    } finally {
      clearTimeout(timeout)
    }
  } catch (err) {
    error = err instanceof Error ? err.message : String(err)
  }

  const wallClockMs = Date.now() - startMs
  const snapshotCount = toolCalls.filter(
    (n) => n === 'take_snapshot' || n === 'take_enhanced_snapshot',
  ).length
  const screenshotCount = toolCalls.filter(
    (n) => n === 'take_screenshot',
  ).length

  // Token estimates
  const estInputTokens =
    Math.round(inputChars / 4) + imageCount * SCREENSHOT_TOKEN_ESTIMATE
  const estOutputTokens = Math.round(outputChars / 4)
  const estTotalTokens = estInputTokens + estOutputTokens

  return {
    scenario,
    version,
    wallClockMs,
    llmStepCount,
    toolCallCount: toolCalls.length,
    toolCalls,
    snapshotCount,
    screenshotCount,
    estInputTokens,
    estOutputTokens,
    estTotalTokens,
    estAvgTokensPerStep:
      llmStepCount > 0 ? Math.round(estTotalTokens / llmStepCount) : 0,
    outputChars,
    inputChars,
    success,
    error,
  }
}

// ---------------------------------------------------------------------------
// printComparison
// ---------------------------------------------------------------------------

export function printComparison(
  baseline: BenchmarkMetrics,
  optimized: BenchmarkMetrics,
): void {
  const pct = (a: number, b: number) =>
    b === 0 ? '  N/A' : `${((1 - a / b) * 100).toFixed(1)}% less`

  const col = (v: number | string, w = 14) => String(v).padStart(w)
  const W = 72

  console.log(`\n${'─'.repeat(W)}`)
  console.log(`  Benchmark: ${baseline.scenario}`)
  console.log('─'.repeat(W))
  console.log(
    `${'Metric'.padEnd(28)}${col('v1 baseline')}${col('v2 optimized')}${col('Δ', 16)}`,
  )
  console.log('─'.repeat(W))

  const row = (label: string, b: number, o: number, unit = '') =>
    console.log(
      `${label.padEnd(28)}${col(b + unit)}${col(o + unit)}${col(pct(o, b), 16)}`,
    )

  row('Wall clock', baseline.wallClockMs, optimized.wallClockMs, 'ms')
  row('LLM steps', baseline.llmStepCount, optimized.llmStepCount)
  row('Tool calls', baseline.toolCallCount, optimized.toolCallCount)
  row('Snapshots', baseline.snapshotCount, optimized.snapshotCount)
  row('Screenshots', baseline.screenshotCount, optimized.screenshotCount)
  row('Input chars', baseline.inputChars, optimized.inputChars)
  row('Output chars', baseline.outputChars, optimized.outputChars)
  row('~Input tokens (est)', baseline.estInputTokens, optimized.estInputTokens)
  row(
    '~Output tokens (est)',
    baseline.estOutputTokens,
    optimized.estOutputTokens,
  )
  row('~Total tokens (est)', baseline.estTotalTokens, optimized.estTotalTokens)
  row(
    '~Tokens/step (est)',
    baseline.estAvgTokensPerStep,
    optimized.estAvgTokensPerStep,
  )

  console.log('─'.repeat(W))
  console.log(
    `  Speed-up:          ${pct(optimized.wallClockMs, baseline.wallClockMs)}`,
  )
  console.log(
    `  Token efficiency:  ${pct(optimized.estTotalTokens, baseline.estTotalTokens)}`,
  )
  console.log(`${'─'.repeat(W)}\n`)
}

// ---------------------------------------------------------------------------
// Utility
// ---------------------------------------------------------------------------

/** Estimated tasks per hour based on observed wall-clock time. */
export function tasksPerHour(wallClockMs: number): number {
  return Math.round(3_600_000 / wallClockMs)
}
