/**
 * Prefix injected into the task for v1 baseline runs.
 * The system prompt's instruction hierarchy grants user messages authority,
 * so this overrides the optimised defaults without server config changes.
 */
export const BASELINE_TASK_PREFIX = `[Benchmark v1 baseline — measurement mode]
For this task only, use the unoptimised approach:
- Use take_screenshot (not take_snapshot) to observe page state.
- Use click and fill sequences for all form interactions (not evaluate_script).
- Do NOT call recall() before starting.
- Do NOT call learn() after finishing.

Task: `

